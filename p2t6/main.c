//计算存款利息
//有N元，想存一年。有3种方法可选：
//（1）活期，年利率为r1；（2）一年定期，年利率为r2；（3）存两次半年定期，年利率为r3。
//请分别计算出一年后按3种方法所得到的本息和。
//r1=0.0036
//r2=0.0225
//r3=0.0198
#include <stdio.h>
int main() {
    int x;
    float r1 = 0.0036, r2 = 0.0225, r3 = 0.0198;
    float p1, p2, p3;
    scanf("%d", &x);
    p1 = x * (r1 + 1);
    p2 = x * (r2 + 1);
    p3 = x * (1 + r3 / 2) * (1 + r3 / 2);
    printf("p1 = %f\n", p1);
    printf("p2 = %f\n", p2);
    printf("p3 = %f\n", p3);
    return 0;
}